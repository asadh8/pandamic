package com.entrue.pandamic.model.util;

public class GeoLocation {

    private double lat;

    private double lon;

    public GeoLocation() {

    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}
